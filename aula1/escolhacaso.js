let estado = "SP";

switch (estado) { // Switch é o equivalente ao escolha caso do visualg
    case "SP":
        console.log("São Paulo");
        break;
    case "MG":
        console.log("Minas Gerais");
        break;
    case "CE":
        console.log("Ceará");
        break;
    default:
        console.log("Estado inválido");
        break;
}